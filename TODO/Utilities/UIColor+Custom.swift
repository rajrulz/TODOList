import Foundation
import UIKit


extension UIColor {
    static func dark() -> UIColor {
        return UIColor.black
    }
    static func light() -> UIColor {
        return UIColor.lightGray
    }
    static func expiredColor() -> UIColor {
        return UIColor.red
    }
}
